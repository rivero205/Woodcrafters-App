    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
 
        if(tablaProductos.getRowCount()>0){
             if(tablaProductos.getSelectedRow()!=-1){
                 int id = Integer.parseInt(String.valueOf(tablaProductos.getValueAt(tablaProductos.getSelectedRow(), 0)));
                 
                 control.borrarProducto(id);
                 
                 mostrarMensaje("Producto eliminado correctamente", "Info", "Borrado de Producto");
                 cargarTabla();
                 
             }
             else{
                 mostrarMensaje("No seleciionó ningún producto", "Error", "Error al eliminar");
             }
        }
        else{
            mostrarMensaje("No hay productos para eliminar", "Info", "Borrado de Producto");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

