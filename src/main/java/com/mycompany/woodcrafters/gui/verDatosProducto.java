    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
       if(tablaProductos.getRowCount()>0){
             if(tablaProductos.getSelectedRow()!=-1){
                 
                 int id = Integer.parseInt(String.valueOf(tablaProductos.getValueAt(tablaProductos.getSelectedRow(), 0)));
                 
                modificarDatosProducto pantallaModif = new modificarDatosProducto(id);
                pantallaModif.setVisible(true);
                pantallaModif.pack();
                pantallaModif.setLocationRelativeTo(null);
                this.dispose();
                
                 
             }
             else{
                 mostrarMensaje("No seleciionó ningún producto", "Error", "Error al editar");
             }
        }
        else{
            mostrarMensaje("No hay productos para editar", "Info", "Modificación de Producto");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

