    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
 
        if(tablaProveedores.getRowCount()>0){
            if(tablaProveedores.getSelectedRow()!=-1){
                int id = Integer.parseInt(String.valueOf(tablaProveedores.getValueAt(tablaProveedores.getSelectedRow(), 0)));
                
                control.borrarProveedor(id);
                
                mostrarMensaje("Proveedor eliminado correctamente", "Info", "Borrado de Proveedor");
                cargarTabla();
                
            }
            else{
                mostrarMensaje("No seleciionó ningún proveedor", "Error", "Error al eliminar"); 
            }
        }else{
            mostrarMensaje("No hay proveedores para eliminar", "Info", "Borrado de Proveedor");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

