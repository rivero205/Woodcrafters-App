    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
        
        //Fila y columna no editable
        @Override
        public boolean isCellEditable(int row, int column){
            return false;
        }
    };
        
     String titulos[] = {"ID", "Nombre", "Direccion", "Telefono"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        List<Proveedores> listaProveedores = control.Proveedores();   
        
        if (listaProveedores != null) {
           for (Proveedores proveedor : listaProveedores) {
               // Verificar si unUsuario no es null
              
               Object[] objeto = {
                   proveedor.getId(),
                   proveedor.getNombre(),
                   proveedor.getDireccion(),
                   proveedor.getTelefono()
               };
                    
                   
               
               modeloTabla.addRow(objeto);
           }

        }
        tablaProveedores.setModel(modeloTabla);
}
