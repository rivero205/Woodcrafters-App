    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
     if(tablaProveedores.getRowCount()>0){
            if(tablaProveedores.getSelectedRow()!=-1){
                
                int id = Integer.parseInt(String.valueOf(tablaProveedores.getValueAt(tablaProveedores.getSelectedRow(), 0)));
                
                modificarDatosProveedor pantallaModif = new modificarDatosProveedor(id);
                pantallaModif.setVisible(true);
                pantallaModif.pack();
                pantallaModif.setLocationRelativeTo(null);
                this.dispose();
                
                
                
            }
            else{
                mostrarMensaje("No seleciionó ningún proveedor", "Error", "Error al editar"); 
            }
        }else{
            mostrarMensaje("No hay proveedores para editar", "Info", "Editado de Proveedor");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

